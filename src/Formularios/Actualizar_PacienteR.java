    private void btnActualizarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarPacienteActionPerformed
        Connection con = null;
        try {
            con = conectarBD.getConnection();
            conectarBD.ps = con.prepareStatement("UPDATE paciente SET Nombre_Paciente=?, Apellido_Paterno_Paciente=?, Apellido_Materno_Paciente=?, Telefono=?, Hora_Entrada=?, Hora_Salida=? WHERE CURP=?");
            conectarBD.ps.setString(1, txtNombrePacienteA.getText());
            conectarBD.ps.setString(2, txtApellidoPaternoPacienteA.getText());
            conectarBD.ps.setString(3, txtApellidoMaternoPacienteA.getText());
            conectarBD.ps.setString(4, txtTelefonoPacienteA.getText());
            conectarBD.ps.setTime(5,Time.valueOf(txtHoraEntradaPacienteA.getText()));
            conectarBD.ps.setTime(6,Time.valueOf(txtHoraSalidaPacienteA.getText()));
            conectarBD.ps.setString(7, txtCurpPacienteA.getText());
            
            //EJECUTAR LA CONSULTA
            int res = conectarBD.ps.executeUpdate();
            if(res > 0){
                JOptionPane.showMessageDialog(null, "PACIENTE ACTUALIZADO");
                limpiarCajas();
            }else{
                JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR PACIENTE");
                limpiarCajas();
            }
            //CERRAR BD
            con.close();
        } catch (Exception e) {
            System.err.println("Error: "+e);
        }   
    }//GEN-LAST:event_btnActualizarPacienteActionPerformed

