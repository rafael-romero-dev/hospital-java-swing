    private void btnBuscarPacienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarPacienteActionPerformed
        Connection con = null;
        try {
            con = conectarBD.getConnection();
            conectarBD.ps = con.prepareStatement("SELECT * FROM paciente WHERE CURP = ?");
            conectarBD.ps.setString(1, txtCurpPacienteA.getText());
            
            conectarBD.rs = conectarBD.ps.executeQuery(); //TRAER LOS RESULTADOS DE LA CONSULTA
            if(conectarBD.rs.next()){
                txtNombrePacienteA.setText(conectarBD.rs.getString("Nombre_Paciente"));
                txtApellidoPaternoPacienteA.setText(conectarBD.rs.getString("Apellido_Paterno_Paciente"));
                txtApellidoMaternoPacienteA.setText(conectarBD.rs.getString("Apellido_Materno_Paciente"));
                txtTelefonoPacienteA.setText(conectarBD.rs.getString("Telefono"));
                txtHoraEntradaPacienteA.setText(conectarBD.rs.getString("Hora_Entrada"));
                txtHoraSalidaPacienteA.setText(conectarBD.rs.getString("Hora_Salida"));
            }else{
                JOptionPane.showMessageDialog(null, "NO SE ENCONTRARON RESULTADOS");
            }
            //EJECUTAR LA CONSULTA
            int res = conectarBD.ps.executeUpdate();
            if(res > 0){
                JOptionPane.showMessageDialog(null, "PACIENTE ACTUALIZADO");
                limpiarCajas();
            }
            else{
                JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR PACIENTE");
                limpiarCajas();
            }
            //CERRAR BD
            con.close();
        } catch (Exception e) {
            System.err.println("Error: "+e);
        }
    }//GEN-LAST:event_btnBuscarPacienteActionPerformed

