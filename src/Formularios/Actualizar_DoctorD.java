    private void btnBuscarDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarDoctorActionPerformed
        Connection con = null;
        try {
            con = conectarBD.getConnection();
            conectarBD.ps = con.prepareStatement("SELECT * FROM doctor WHERE Id_Doctor = ?");
            conectarBD.ps.setString(1, txtId_DoctorA.getText());

            conectarBD.rs = conectarBD.ps.executeQuery(); //TRAER LOS RESULTADOS DE LA CONSULTA
            if(conectarBD.rs.next()){
                txtNombreDoctorA.setText(conectarBD.rs.getString("Nombre_Doctor"));
                txtApellidoPaternoDoctorA.setText(conectarBD.rs.getString("Apellido_Paterno_Doctor"));
                txtApellidoMaternoDoctorA.setText(conectarBD.rs.getString("Apellido_Materno_Doctor"));
                txtEspecialidadA.setText(conectarBD.rs.getString("Especialidad"));
            }else{
                JOptionPane.showMessageDialog(null, "NO SE ENCONTRARON RESULTADOS");
            }
            //EJECUTAR LA CONSULTA
            int res = conectarBD.ps.executeUpdate();
            if(res > 0){
                JOptionPane.showMessageDialog(null, "DOCTOR ACTUALIZADO");
                limpiarCajas();
            }
            else{
                JOptionPane.showMessageDialog(null, "VERIFICA LA CLAVE DEL DOCTOR");
                limpiarCajas();
            }
            //CERRAR BD
            con.close();
        } catch (Exception e) {
            System.err.println("Error: "+e);
        }
    }//GEN-LAST:event_btnBuscarDoctorActionPerformed

