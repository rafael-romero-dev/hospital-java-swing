    private void btnActualizarDoctorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarDoctorActionPerformed
        Connection con = null;
        try {
            con = conectarBD.getConnection();
            conectarBD.ps = con.prepareStatement("UPDATE doctor SET Nombre_Doctor=?, Apellido_Paterno_Doctor=?, Apellido_Materno_Doctor=?, Especialidad=? WHERE Id_Doctor=?");
            conectarBD.ps.setString(1, txtNombreDoctorA.getText());
            conectarBD.ps.setString(2, txtApellidoPaternoDoctorA.getText());
            conectarBD.ps.setString(3, txtApellidoMaternoDoctorA.getText());
            conectarBD.ps.setString(4, txtEspecialidadA.getText());
            conectarBD.ps.setString(5, txtId_DoctorA.getText());

            //EJECUTAR LA CONSULTA
            int res = conectarBD.ps.executeUpdate();
            if(res > 0){
                JOptionPane.showMessageDialog(null, "DOCTOR ACTUALIZADO");
                limpiarCajas();
            }else{
                JOptionPane.showMessageDialog(null, "ERROR AL ACTUALIZAR DOCTOR");
                limpiarCajas();
            }
            //CERRAR BD
            con.close();
        } catch (Exception e) {
            System.err.println("Error: "+e);
        }
    }//GEN-LAST:event_btnActualizarDoctorActionPerformed

